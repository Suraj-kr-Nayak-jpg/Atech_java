public class Bitwise_AND {
    public static void main(String[] args){
        int a = 5; //0101
        int b = 6; //0110
        System.out.println(a&b);


        int C = 8; //1000
        int D = 9; //1001
        System.out.println(C&D);


        int E = 11; //1011
        int F = 4; //0100
        System.out.println(E&F);
    }
}
